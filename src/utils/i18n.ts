export type TranslationKey = keyof typeof translations.en;

export function normalizeLanguage(value: unknown): Language {
  return supportedLanguages.includes(value as Language) ? (value as Language) : defaultLanguage;
}

export function translate(language: Language, key: TranslationKey, replacements?: Record<string, string | number>) {
  let value: string = translations[language][key] ?? translations[defaultLanguage][key];

  if (replacements) {
    Object.entries(replacements).forEach(([name, replacement]) => {
      value = value.replaceAll(`{${name}}`, String(replacement));
    });
  }

  return value;
}
